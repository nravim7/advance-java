package org.ravi;

public class Floats {

    public static void main(String[] args) {
        float floatValue = (float) 5.25;
        System.out.println(floatValue);


        //Challenge Pounds to Kilogram conversion
        double poundValue = 200d;
        double convertedToKilograms = (poundValue * 0.45359237);
        System.out.println(poundValue + " pounds is equal to " + convertedToKilograms + "Kgs");

    }


}
